export function PostContent() {
  const [titleEditable, setTitleEditable] = useState(false);
  const [postTitle, setPostTitle] = useState("");
  const [text, setText] = useState("");
  const [editorState, setEditorState] = useState(() =>
    EditorState.createEmpty(),
  );

  return (
    <div className="w-1/2 overflow-auto border-r p-6">
      <div className="mb-4 flex items-center space-x-2">
        <h2 className="cursor-pointer text-lg font-bold">
          <div className="flex items-center">
            {!titleEditable && postTitle && (
              <div className="flex items-center rounded-full px-2 py-1 hover:bg-gray-100">
                <span onClick={() => setTitleEditable(true)} className="mr-2">
                  Post Title
                </span>
                <PenIcon className="h-4 w-4" />
              </div>
            )}
            {titleEditable || !postTitle ? (
              <Input
                className="w-full border-none bg-transparent"
                defaultValue={postTitle}
                placeholder="Post Title"
                onBlur={() => setTitleEditable(false)}
              />
            ) : (
              <span onClick={() => setTitleEditable(true)}>{postTitle}</span>
            )}
          </div>
        </h2>
      </div>
      <div className="space-y-4">
        <Editor
          editorState={editorState}
          onEditorStateChange={setEditorState}
          toolbar={{
            options: [
              "inline",
              "blockType",
              "list",
              "textAlign",
              "link",
              "image",
            ],
            inline: {
              options: ["bold", "italic", "underline", "strikethrough"],
            },
          }}
        />
      </div>
    </div>
  );
}

